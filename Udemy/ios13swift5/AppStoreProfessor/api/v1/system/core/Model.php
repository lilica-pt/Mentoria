class CI_Model {

	/**
	 * Class constructor
	 *
	 * @return	void
	 */
	public function __construct()
	{
		log_message('info', 'Model Class Initialized');
	}

	// --------------------------------------------------------------------

	/**
	 * __get magic
	 *
	 * Allows models to access CI's loaded classes using the same
	 * syntax as controllers.
	 *
	 * @param	string	$key
	 */
	public function __get($key)
	{
		// Debugging note:
		//	If you're here because you're getting an error message
		//	saying 'Undefined Property: system/core/Model.php', it's
		//	most likely a typo in your model code.
		return get_instance()->$key;
	}

	public function urlJson($data){
		return base_url()."public/db/".$data;
	}

	public function urlIcone($data){
		return base_url()."public/upload/icones/".$data;
	}

	public function urlCapa($data){
		return base_url()."public/upload/capas/".$data;
	}

	public function urlScreenshots($data){
		return base_url()."public/upload/screenshots/".$data;
	}

	public function sanitizeString($string) {
    $what = array( 'ä','ã','à','á','â','ê','ë','è','é','ï','ì','í','ö','õ','ò','ó','ô','ü','ù','ú','û','À','Á','É','Í','Ó','Ú','ñ','Ñ','ç','Ç',' ','-','(',')',',',';',':','|','!','"','#','$','%','&','/','=','?','~','^','>','<','ª','º' );
    $by   = array( 'a','a','a','a','a','e','e','e','e','i','i','i','o','o','o','o','o','u','u','u','u','A','A','E','I','O','U','n','n','c','C',' ','','','','','','','','','','','','','','','','','','','','','','' );
    return str_replace($what, $by, $string);
	}
	
	public function numerosAleatorios($min, $max, $qtd){
		$contador = 0;
		$valores = array();
		do {
	    $valor = rand($min, $max);
	    if (in_array($valor, $valores) == false) {
        $contador++;
				array_push($valores, $valor);
	    }
		} while ($contador < $qtd);
		return $valores;
	}

}
